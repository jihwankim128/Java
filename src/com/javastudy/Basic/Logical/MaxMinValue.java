package com.javastudy.Basic.Logical;

public class MaxMinValue {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println("Max Value = " +
                ((a > b) ? a : b));
        System.out.println("Min Value = " +
                ((a < b) ? a : b));
    }
}
