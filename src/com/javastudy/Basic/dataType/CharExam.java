package com.javastudy.Basic.dataType;

public class CharExam {
    public static void main(String[] args) {
        char one = '1';
        char two = '2';
        char three = '3';
        char four = '4';
        char five = '5';

        int sum = one-'0' + two-'0' + three-'0'
                + four-'0' + five-'0';
        System.out.println("'1'+'2'+'3'+'4'+'5'" +
                " = " + sum);
    }
}
